public class SumAndAverage {
    public static void main(String args[])
    {
        int sum=0;
        for(int i=0;i<=100;i++)
        {
            sum=sum+i;
                    }
        System.out.println("sum of 100 no is --  "+sum);
        System.out.println("average of 100 no is --  "+sum/100);
    }
}
