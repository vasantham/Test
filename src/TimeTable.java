public class TimeTable {
    public static void main(String args[])
    {
        for(int i=1;i<=9;i++)
        {
            for(int j=1;j<=9;j++)
            {
                System.out.print(i*j);
            }
            System.out.println();
        }

        for(int i=1;i<=12;i++)
        {
            for(int j=1;j<=12;j++)
            {
                System.out.print(i*j);
            }
            System.out.println();
        }
    }
}
